<template>
    <div class="container">
        <h1>register</h1>
    </div>
</template>
<script>
export default {
    beforeRouteEnter(to, from, next) {
        console.log(to, from)
        alert('你好，这里是组件内路由前置守卫beforeRouteEnter，此时将要进入该路由对应的组件，这时组件实例还没创建呢，不能使用实例this，可以再此时对路由做些改变')
        next()
    },
    beforeRouteLeave(to, from, next) {
        console.log(to, from);
        alert('真不好意思，即将离开当前路由，进入下一个路由，很期待下次和你见面，这里是组件内路由离开守卫beforeRouteLeave');
        next();
    }
}
</script>